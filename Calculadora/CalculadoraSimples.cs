namespace Calculadora
{
    public class CalculadoraSimples
    {
        //Método para adicionar 2 números
        public int Adicionar(int num1, int num2)
        {
            return num1 + num2;
        }

        //Método para multiplicar 2 números
        public int Multiplicar(int num1, int num2)
        {
            return num1 + num2;
        }
    }
}
